#include "function_pointers.h"
#include "3-calc.h"
/**
 * op_add - func
 * @a: input
 * @b: input
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - func
 * @a: input
 * @b: input
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - func
 * @a: input
 * @b: input
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - func
 * @a: input
 * @b: input
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - func
 * @a: input
 * @b: input
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
